package com.classexample11;

public class FloatSubtraction {
	float firstnumber = 30.54f;
	float secondnumber = 14.54f;
	public void add() {
		float sub = firstnumber+secondnumber;
		System.out.println("subtraction="+sub);
	}

	public static void main(String[] args) {
		FloatSubtraction xyz = new FloatSubtraction();
		xyz.add();

	}

}
