package com.classexample11;

public class Floatmultiplication {
	float firstnumber = 30.90f;
	float secondnumber = 2.90f;
	public void add() {
		float mul = firstnumber*secondnumber;
		System.out.println("multiplication="+mul);
	}

	public static void main(String[] args) {
		Floatmultiplication xyz = new Floatmultiplication();
		xyz.add();
		
	}

}
